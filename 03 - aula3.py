"""
Python - Liguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples 
print('Lucas Sabatini')
print(1, 'Lucas "Sabatini"')

# Aspas duplas
print("Lucas Sabatini")
print(2, "Lucas 'Sabatini'")

# Escape \
print("Lucas \"Sabatini\"")

# r
print(r"Lucas \"Sabatini\"")
