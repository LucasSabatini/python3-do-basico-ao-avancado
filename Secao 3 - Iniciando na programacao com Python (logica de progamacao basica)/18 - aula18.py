# if / elif / else
# se / senão se / senão
condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = True

if condicao1:
    print('Este é o código para condição 1')
elif condicao2:
    print('Este é o código para condição 2')
elif condicao3:
    print('Este é o código para condição 3')
elif condicao4:
    print('Este é o código para condição 4')
else:
    print('Nenhuma condição satisfeita')

